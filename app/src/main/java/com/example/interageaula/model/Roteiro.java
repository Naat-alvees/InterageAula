package com.example.interageaula.model;

public class Roteiro {
    private String tituloRoteiro;
    private String subtituloRoteiro;
    private String dataRoteiro;


    public Roteiro() {
    }


    public String getTituloRoteiro() {
        return tituloRoteiro;
    }

    public void setTituloRoteiro(String tituloRoteiro) {
        this.tituloRoteiro = tituloRoteiro;
    }

    public String getSubtituloRoteiro() {
        return subtituloRoteiro;
    }

    public void setSubtituloRoteiro(String subtituloRoteiro) {
        this.subtituloRoteiro = subtituloRoteiro;
    }

    public String getDataRoteiro() {
        return dataRoteiro;
    }

    public void setDataRoteiro(String dataRoteiro) {
        this.dataRoteiro = dataRoteiro;
    }
    
}
